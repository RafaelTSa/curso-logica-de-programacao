# Tuplas
# Tuplas são como listas, mas imutáveis. Elas são criadas com parênteses ( ).

cores = ("vermelho", "azul", "verde")
numeros = (1, 2, 3, 4, 5)

print(cores)
print(numeros)

# Acessando elementos
print(cores[0]) # "vermelho"
print (cores[-1]) # verde
print(numeros[2]) # 3
print(numeros[-2]) # 4