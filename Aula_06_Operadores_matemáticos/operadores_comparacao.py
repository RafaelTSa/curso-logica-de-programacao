# Operadores de comparação: ==, !=, >, <, >=, <=
print(2 == 2) #compara dois números com operador de igualdade " == " retorna "true"
print(2 == 3) #compara dois numeros com operador de igauldade "==" retorna "false"
print(4 != 2) #compara dois numeros com operador de diferente "!=" retorna "true"
print(4 != 4) #compara dois numeros com operador de diferente "!=" retorna "false"
print(4 != 4) #compara dois numeros com operador de diferente "!=" retorna "false"
print(4 > 2) #compara dois números com operador de maior " > " retorna "true"
print(4 > 9) #compara dois números com operador de maior " > " retorna "false"